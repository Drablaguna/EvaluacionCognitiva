-- Porcentaje de entendimiento TOTAL de a por edad
-- 100% -> COUNT(*) * 21 - instancias * 21 max puntos por celda
-- CASO PARTICULAR, en 12 y en 16 hay filas que pusieron 10, 19 y 20 de edad por broma, pero aun asi se cuentan
SELECT (SUM(total_a)) / (COUNT(*) * 21) * 100 FROM secu_todo WHERE edad IN (10,12);
-- 12 -> 64.3261%
SELECT (SUM(total_a)) / (COUNT(*) * 21) * 100 FROM secu_todo WHERE edad = 13;
-- 13 -> 64.1049%
-- 14 -> 68.6012%
-- 15 -> 71.4881%
SELECT (SUM(total_a)) / (COUNT(*) * 21) * 100 FROM secu_todo WHERE edad IN (16,19,20);
-- 16 -> 69.8413%

